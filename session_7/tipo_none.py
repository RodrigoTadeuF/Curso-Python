"""
Tipo None

O tipo de dado None em Python representa o tipo sem um tipo, ou poderia ser conhecido também
como tipo vazio, porém falar que é um tipo sem tipo é mais apropriado.

OBS: o tipo None é SEMPRE especificado com a letra maiúscula.

Certo: None,
Errado: none

Quando utilizamos?

- Podemos utilizar None qunado queremos criar uma variável e queremos inicializá-la sem um tipo, antes
de recebermos um valor final.
"""

numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.67

print(numeros)
print(type(numeros))